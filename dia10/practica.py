#Creacion del diccionario
tecnologia = {
    1: "celular",
    2: "pc"
}
print(tecnologia)

#Agregar elemento
tecnologia[3] = "tablet"
print(tecnologia)

#Cambiar el valor a una clave
tecnologia[2] = "mouse"
print(tecnologia)

#Eliminar clave:valor del diccionario
del tecnologia[1]
print(tecnologia)
print("Fin del programa")